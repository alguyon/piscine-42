char	*ft_strstr(char *str, char *to_find)
{
	int		i1;
	int		i2;

	i1 = 0;
	i2 = 0;
	if (to_find[0] == '\0')
		return (str);
	while (str[i1] != '\0')
	{
		while (str[i1 + i2] == to_find[i2])
		{
			if (to_find[i2 + 1] == '\0')
				return (str + i1);
			i2++;
		}
		i1++;
	}
	return (0);
}
